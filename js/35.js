/* Fetch API
el reemplazo a Ajax (XMLHttpRequest)

Fetch API es el nuevo estándar
igual que ajax te permite enviar inforación al
servidor u obtener información de un servidor.
Tambien te va a permitira actualizar el sitio web sin 
recargar la pagina.
Va a enviar los datos, los va a procesar atra de escena
y va a actualizar en tiempo real nuestro sitio web

Tambien vamos a obtener un archivo local o respuesta
de un servidor (Texto o JSON)

El JS que vimos hasta aqui no puede, el JS para
web no puede consultar una base de datos por si 
solo, requiere un lenguaje de backend que se conecta 
a un servidor, se conecta a una base de datos
y realiza la consulta.

Esa consulta, para poderla consumir en JS, tiene que
estar en un formato llamado JSON.

Al igual que todas las API's Modernas da JS utiliza
Promises o también async / await
 */


/*  JSON
    Javascript Object Notation
    Lenuaje de Transporte de Datos
Un JSON puede ser creado en cualquier lenguaje y se puede
consumir en JS por medio de Fetch API y mostrarlo en tu
sitio web.
Cualquier lenguaje de backend:
    Python
    C#
    PHP
    Node.js
    Java
Pueden exportar una respuesta en un JSON, que podemos 
consumir en Javascript con Fetch API y mostrar los 
resultados
 */